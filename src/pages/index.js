import Head from "next/head";
import { CardFilm } from "@/components/cardFilm";
import style from "@/styles/home.module.css";

import { useContext, useEffect } from "react";
import { filmContext } from "@/context/film-contexct";

export default function Home() {
  const { data, getFilm, loading, title } = useContext(filmContext);
  useEffect(() => {
    getFilm();
  }, [getFilm]);

  if (!loading) {
    console.log(loading);
    return (
      <div className={style.cardContainer}>
        <div className="spinner-grow text-dark" role="status">
          <span className="visually-hidden">Loading...</span>
        </div>
      </div>
    );
  }

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className={`main`}>
        <h1 className="mt-4 text-center"> {title} </h1>
        <div className={style.cardContainer}>
          {data?.map((film) => {
            const baseUrl = "https://image.tmdb.org/t/p/w500/";
            const fullUrl = baseUrl + film.backdrop_path;

            return (
              <div key={film.id}>
                <CardFilm
                  filmImg={fullUrl}
                  filmName={film.title}
                  filmDate={film.release_date}
                  filmRate={film.vote_average}
                  id={film.id}
                />
              </div>
            );
          })}
        </div>
      </main>
    </>
  );
}

